import { useFileHandler, useInputValidation } from "6pp";
import { CameraAlt as CameraAltIcon } from "@mui/icons-material";
import {
  Avatar,
  Button,
  Container,
  FormControl,
  IconButton,
  InputLabel,
  MenuItem,
  Paper,
  Select,
  Stack,
  TextField,
  Typography,
} from "@mui/material";
import axios from "axios";
import React, { useState } from "react";
import toast from "react-hot-toast";
import { useDispatch } from "react-redux";
import { VisuallyHiddenInput } from "../components/styles/StyledComponents";
import { bgGradient } from "../constants/color";
import { server } from "../constants/config";
import { userExists } from "../redux/reducers/auth";
import { usernameValidator } from "../utils/validators";

const Login = () => {
  const [isLogin, setIsLogin] = useState(true);
  const [isLoading, setIsLoading] = useState(false);
  const [language, setLanguage] = useState("");

  const languagehandler = (event) => {
    setLanguage(event.target.value);
  };

  const toggleLogin = () => setIsLogin((prev) => !prev);

  const name = useInputValidation("");
  const bio = useInputValidation("");
  const username = useInputValidation("", usernameValidator);
  const password = useInputValidation("");
  //const language = useInputValidation("")

  const avatar = useFileHandler("single");

  const dispatch = useDispatch();

  const handleLogin = async (e) => {
    e.preventDefault();

    const toastId = toast.loading("Logging In...");

    setIsLoading(true);
    const config = {
      withCredentials: true,
      headers: {
        "Content-Type": "application/json",
      },
    };

    try {
      const { data } = await axios.post(
        `${server}/api/v1/user/login`,
        {
          username: username.value,
          password: password.value,
        },
        config
      );
      dispatch(userExists(data.user));
      toast.success(data.message, {
        id: toastId,
      });
    } catch (error) {
      toast.error(error?.response?.data?.message || "Something Went Wrong", {
        id: toastId,
      });
    } finally {
      setIsLoading(false);
    }
  };

  const handleSignUp = async (e) => {
    e.preventDefault();

    const toastId = toast.loading("Signing Up...");
    setIsLoading(true);

    const formData = new FormData();
    formData.append("avatar", avatar.file);
    formData.append("name", name.value);
    formData.append("bio", bio.value);
    formData.append("username", username.value);
    formData.append("password", password.value);
    formData.append("language", language);

    const config = {
      withCredentials: true,
      headers: {
        "Content-Type": "multipart/form-data",
      },
    };

    try {
      const { data } = await axios.post(
        `${server}/api/v1/user/new`,
        formData,
        config
      );

      dispatch(userExists(data.user));
      toast.success(data.message, {
        id: toastId,
      });
    } catch (error) {
      toast.error(error?.response?.data?.message || "Something Went Wrong", {
        id: toastId,
      });
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <div
      style={{
        backgroundImage: bgGradient,
      }}
    >
      <Container
        component={"main"}
        maxWidth="xs"
        sx={{
          height: "100vh",
          display: "flex",
          justifyContent: "center",
          alignItems: "center",
        }}
      >
        <Paper
          elevation={3}
          sx={{
            padding: 4,
            display: "flex",
            flexDirection: "column",
            alignItems: "center",
          }}
        >
          {isLogin ? (
            <>
              <Typography variant="h5">Login</Typography>
              <form
                style={{
                  width: "100%",
                  marginTop: "1rem",
                }}
                onSubmit={handleLogin}
              >
                <TextField
                  required
                  fullWidth
                  label="Username"
                  margin="normal"
                  variant="outlined"
                  value={username.value}
                  onChange={username.changeHandler}
                />

                <TextField
                  required
                  fullWidth
                  label="Password"
                  type="password"
                  margin="normal"
                  variant="outlined"
                  value={password.value}
                  onChange={password.changeHandler}
                />

                <Button
                  sx={{
                    marginTop: "1rem",
                  }}
                  variant="contained"
                  color="primary"
                  type="submit"
                  fullWidth
                  disabled={isLoading}
                >
                  Login
                </Button>

                <Typography textAlign={"center"} m={"1rem"}>
                  OR
                </Typography>

                <Button
                  disabled={isLoading}
                  fullWidth
                  variant="text"
                  onClick={toggleLogin}
                >
                  Sign Up Instead
                </Button>
              </form>
            </>
          ) : (
            <>
              <Typography variant="h5">Sign Up</Typography>
              <form
                style={{
                  width: "100%",
                  marginTop: "1rem",
                }}
                onSubmit={handleSignUp}
              >
                <Stack position={"relative"} width={"10rem"} margin={"auto"}>
                  <Avatar
                    sx={{
                      width: "10rem",
                      height: "10rem",
                      objectFit: "contain",
                    }}
                    src={avatar.preview}
                  />

                  <IconButton
                    sx={{
                      position: "absolute",
                      bottom: "0",
                      right: "0",
                      color: "white",
                      bgcolor: "rgba(0,0,0,0.5)",
                      ":hover": {
                        bgcolor: "rgba(0,0,0,0.7)",
                      },
                    }}
                    component="label"
                  >
                    <>
                      <CameraAltIcon />
                      <VisuallyHiddenInput
                        type="file"
                        onChange={avatar.changeHandler}
                      />
                    </>
                  </IconButton>
                </Stack>

                {avatar.error && (
                  <Typography
                    m={"1rem auto"}
                    width={"fit-content"}
                    display={"block"}
                    color="error"
                    variant="caption"
                  >
                    {avatar.error}
                  </Typography>
                )}

                <TextField
                  required
                  fullWidth
                  label="Name"
                  margin="normal"
                  variant="outlined"
                  value={name.value}
                  onChange={name.changeHandler}
                />
                <FormControl sx={{ width: "100%" }}>
                  <InputLabel id="language-label">Language</InputLabel>
                  <Select label="Language" labelId="language-label" value={language} onChange={languagehandler}>
                    {/* Indian Languages */}
                    <MenuItem value={"hi"}>Hindi</MenuItem>
                    <MenuItem value={"bn"}>Bengali</MenuItem>
                    <MenuItem value={"te"}>Telugu</MenuItem>
                    <MenuItem value={"mr"}>Marathi</MenuItem>
                    <MenuItem value={"ta"}>Tamil</MenuItem>
                    <MenuItem value={"ur"}>Urdu</MenuItem>
                    <MenuItem value={"gu"}>Gujarati</MenuItem>
                    <MenuItem value={"kn"}>Kannada</MenuItem>
                    <MenuItem value={"ml"}>Malayalam</MenuItem>
                    <MenuItem value={"pa"}>Punjabi</MenuItem>
                    <MenuItem value={"or"}>Odia</MenuItem>
                    <MenuItem value={"as"}>Assamese</MenuItem>
                    <MenuItem value={"mr"}>Maithili</MenuItem>
                    <MenuItem value={"sd"}>Sindhi</MenuItem>
                    <MenuItem value={"ne"}>Nepali</MenuItem>
                    <MenuItem value={"mr"}>Marwari</MenuItem>
                    {/* Other Popular Languages */}
                    <MenuItem value={"en"}>English</MenuItem>
                    <MenuItem value={"es"}>Spanish</MenuItem>
                    <MenuItem value={"zh"}>Chinese</MenuItem>
                    <MenuItem value={"fr"}>French</MenuItem>
                    <MenuItem value={"ar"}>Arabic</MenuItem>
                    <MenuItem value={"pt"}>Portuguese</MenuItem>
                    <MenuItem value={"ru"}>Russian</MenuItem>
                    <MenuItem value={"de"}>German</MenuItem>
                    <MenuItem value={"ja"}>Japanese</MenuItem>
                    <MenuItem value={"ko"}>Korean</MenuItem>
                    <MenuItem value={"it"}>Italian</MenuItem>
                    <MenuItem value={"tr"}>Turkish</MenuItem>
                    <MenuItem value={"vi"}>Vietnamese</MenuItem>
                    <MenuItem value={"pl"}>Polish</MenuItem>
                    <MenuItem value={"uk"}>Ukrainian</MenuItem>
                    <MenuItem value={"nl"}>Dutch</MenuItem>
                    <MenuItem value={"sv"}>Swedish</MenuItem>
                    <MenuItem value={"th"}>Thai</MenuItem>
                    <MenuItem value={"el"}>Greek</MenuItem>
                    <MenuItem value={"cs"}>Czech</MenuItem>
                    <MenuItem value={"ro"}>Romanian</MenuItem>
                    <MenuItem value={"hu"}>Hungarian</MenuItem>
                    <MenuItem value={"fi"}>Finnish</MenuItem>
                    <MenuItem value={"da"}>Danish</MenuItem>
                    {/* Add more languages as needed */}
                  </Select>
                </FormControl>

                <TextField
                  required
                  fullWidth
                  label="Bio"
                  margin="normal"
                  variant="outlined"
                  value={bio.value}
                  onChange={bio.changeHandler}
                />
                <TextField
                  required
                  fullWidth
                  label="Username"
                  margin="normal"
                  variant="outlined"
                  value={username.value}
                  onChange={username.changeHandler}
                />

                {username.error && (
                  <Typography color="error" variant="caption">
                    {username.error}
                  </Typography>
                )}

                <TextField
                  required
                  fullWidth
                  label="Password"
                  type="password"
                  margin="normal"
                  variant="outlined"
                  value={password.value}
                  onChange={password.changeHandler}
                />

                <Button
                  sx={{
                    marginTop: "1rem",
                  }}
                  variant="contained"
                  color="primary"
                  type="submit"
                  fullWidth
                  disabled={isLoading}
                >
                  Sign Up
                </Button>

                <Typography textAlign={"center"} m={"1rem"}>
                  OR
                </Typography>

                <Button
                  disabled={isLoading}
                  fullWidth
                  variant="text"
                  onClick={toggleLogin}
                >
                  Login Instead
                </Button>
              </form>
            </>
          )}
        </Paper>
      </Container>
    </div>
  );
};

export default Login;
